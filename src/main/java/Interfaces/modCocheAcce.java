    // Carga los datos de la marca seleccionada en la tabla y los muestra en los campos de texto

    private void mostrarDatosEditar() {
       
        int fila = filaSeleccionadaJTable(padre.getJTable());
        
        int id = (int) padre.getJTable().getValueAt(fila, 0);
        
        this.cocheAcce = padre.getCac().findById(id);
        
        String idtext = this.cocheAcce.getIdRegistro()+ "";
        
        List<Coches> coches = this.cc.findAll();
        List<Accesorios> accesorios = this.ac.findAll();
        for (Coches coche : coches) {
            this.selecCoche.addItem(coche.getIdCoche() + ", " + coche.getModelo());
        }
        
        for (Accesorios accesorio : accesorios) {
            this.selecAcce.addItem(accesorio.getIdAccesorio() + ", " + accesorio.getNombre());
        }
        this.idRegistro.setBackground(Color.GRAY);
        this.idRegistro.setText(idtext);
        int idCoche = this.cocheAcce.getIdCoche().getIdCoche();
        String nomCoche = this.cocheAcce.getIdCoche().getModelo();
        String cocheAsociado = idCoche + ", " + nomCoche;
        int idAcce = this.cocheAcce.getIdAccesorio().getIdAccesorio();
        String nomAcce = this.cocheAcce.getIdAccesorio().getNombre();
        String acceAsociado = idAcce + ", " + nomAcce;
        
        this.selecCoche.setSelectedItem(cocheAsociado);
        this.selecAcce.setSelectedItem(acceAsociado);
    }
 // Muestra los datos en los campos de texto
    private int filaSeleccionadaJTable(JTable jTable1) {
        int fila = jTable1.getSelectedRow();
        return fila;
    }

